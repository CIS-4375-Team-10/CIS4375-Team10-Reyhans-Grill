-- Create the main database once if it does not exist.
CREATE DATABASE IF NOT EXISTS reyhans_grill
  DEFAULT CHARACTER SET utf8mb4
  DEFAULT COLLATE utf8mb4_unicode_ci;

USE reyhans_grill;

-- Category lookup table (Fruits, Vegetables, etc.).
CREATE TABLE IF NOT EXISTS Category (
  Category_ID VARCHAR(20) PRIMARY KEY,
  Category_Name VARCHAR(100) NOT NULL,
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  Updated_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Global thresholds used when items do not supply their own values.
CREATE TABLE IF NOT EXISTS Inventory_Settings (
  Setting_ID INT UNSIGNED PRIMARY KEY AUTO_INCREMENT,
  Low_Stock_Threshold INT UNSIGNED NOT NULL DEFAULT 10,
  Expiring_Soon_Days INT UNSIGNED NOT NULL DEFAULT 7,
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  Updated_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- User accounts (admin plus future staff logins).
CREATE TABLE IF NOT EXISTS user (
  User_ID VARCHAR(20) PRIMARY KEY,
  Username VARCHAR(50) NOT NULL UNIQUE,
  Password VARCHAR(255) NOT NULL,
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  Updated_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Master inventory table that stores every material/utensil.
CREATE TABLE IF NOT EXISTS Item (
  Item_ID VARCHAR(20) PRIMARY KEY,
  Item_Name VARCHAR(120) NOT NULL,
  Category_ID VARCHAR(20) NOT NULL,
  Quantity_in_Stock INT UNSIGNED NOT NULL DEFAULT 0,
  Unit VARCHAR(50) NOT NULL DEFAULT 'each',
  Unit_Cost DECIMAL(10, 2) NOT NULL DEFAULT 0.00,
  Shelf_Life_Days INT UNSIGNED DEFAULT 0,
  Expiration_Date DATE,
  Status ENUM('AVAILABLE', 'LOW', 'OUT_OF_STOCK') NOT NULL DEFAULT 'AVAILABLE',
  Item_Type ENUM('MATERIAL', 'UTENSIL', 'OTHER') NOT NULL DEFAULT 'MATERIAL',
  Low_Stock_Threshold INT UNSIGNED DEFAULT NULL,
  Expiring_Soon_Days INT UNSIGNED DEFAULT NULL,
  Par_Level INT UNSIGNED DEFAULT 0,
  Reorder_Point INT UNSIGNED DEFAULT 0,
  Is_Deleted TINYINT(1) NOT NULL DEFAULT 0,
  Deleted_At TIMESTAMP NULL DEFAULT NULL,
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  Updated_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_item_category FOREIGN KEY (Category_ID) REFERENCES Category (Category_ID)
    ON UPDATE CASCADE
    ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Legacy usage log kept for backwards compatibility.
CREATE TABLE IF NOT EXISTS `usage` (
  Usage_ID VARCHAR(24) PRIMARY KEY,
  Item_ID VARCHAR(20) NOT NULL,
  Quantity_Used INT UNSIGNED NOT NULL,
  Date_Used DATE NOT NULL,
  User_ID VARCHAR(20) NOT NULL,
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  Updated_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_usage_item FOREIGN KEY (Item_ID) REFERENCES Item (Item_ID)
    ON UPDATE CASCADE
    ON DELETE CASCADE,
  CONSTRAINT fk_usage_user FOREIGN KEY (User_ID) REFERENCES user (User_ID)
    ON UPDATE CASCADE
    ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Newer inventory usage entries logged from the UI.
CREATE TABLE IF NOT EXISTS Inventory_Usage (
  Usage_ID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  Item_ID VARCHAR(20) NOT NULL,
  Used_Quantity INT UNSIGNED NOT NULL,
  Usage_Date DATE NOT NULL DEFAULT (CURRENT_DATE),
  Notes VARCHAR(255),
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_inventory_usage_item FOREIGN KEY (Item_ID) REFERENCES Item (Item_ID)
    ON UPDATE CASCADE
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Historical material usage table (allows decimals for old data).
CREATE TABLE IF NOT EXISTS Material_Usage (
  Usage_ID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  Item_ID VARCHAR(20) NOT NULL,
  Usage_Date DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  Quantity_Used DECIMAL(10, 2) NOT NULL,
  Reason VARCHAR(100),
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_material_usage_item FOREIGN KEY (Item_ID) REFERENCES Item (Item_ID)
    ON UPDATE CASCADE
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Electronic income sources (DoorDash, Card, etc.).
CREATE TABLE IF NOT EXISTS Electronic_Income (
  Electronic_ID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  Income_Date DATE NOT NULL,
  Channel VARCHAR(40) NOT NULL,
  Amount DECIMAL(12, 2) NOT NULL,
  Notes VARCHAR(120),
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  Updated_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Cash deposits counted at the end of each day.
CREATE TABLE IF NOT EXISTS Cash_Income (
  Cash_ID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  Income_Date DATE NOT NULL,
  Amount DECIMAL(12, 2) NOT NULL,
  Notes VARCHAR(120),
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  Updated_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Manual expenses keyed in from bank/receipt records.
CREATE TABLE IF NOT EXISTS Expense (
  Expense_ID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  Expense_Date DATE NOT NULL,
  Payment_Type VARCHAR(40) NOT NULL,
  Paid_To VARCHAR(120) NOT NULL,
  Description VARCHAR(255),
  Amount DECIMAL(12, 2) NOT NULL,
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  Updated_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Session table for the web app. Keeps the user logged in.
CREATE TABLE IF NOT EXISTS user_sessions (
  Session_ID BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  User_ID VARCHAR(20) NOT NULL,
  Session_Token CHAR(96) NOT NULL,
  Created_At DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  Last_Activity_At DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_user_sessions_user FOREIGN KEY (User_ID) REFERENCES user (User_ID)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
  UNIQUE KEY uq_session_token (Session_Token)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Purchase log ties inventory buys to users and total cost.
CREATE TABLE IF NOT EXISTS Purchase (
  Purchase_ID VARCHAR(24) PRIMARY KEY,
  User_ID VARCHAR(20) NOT NULL,
  Item_ID VARCHAR(20) NOT NULL,
  Quantity_Purchased INT UNSIGNED NOT NULL,
  Purchase_Date DATE NOT NULL,
  Total_Cost DECIMAL(12, 2) NOT NULL DEFAULT 0.00,
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  Updated_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_purchase_item FOREIGN KEY (Item_ID) REFERENCES Item (Item_ID)
    ON UPDATE CASCADE
    ON DELETE CASCADE,
  CONSTRAINT fk_purchase_user FOREIGN KEY (User_ID) REFERENCES user (User_ID)
    ON UPDATE CASCADE
    ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- Optional reporting table (currently unused but kept for future use).
CREATE TABLE IF NOT EXISTS Report (
  Report_ID VARCHAR(40) PRIMARY KEY,
  Report_Date DATE NOT NULL,
  Total_Spent DECIMAL(12, 2) NOT NULL DEFAULT 0.00,
  Total_Used_Items INT UNSIGNED NOT NULL DEFAULT 0,
  User_ID VARCHAR(20) NOT NULL,
  Created_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  Updated_At TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_report_user FOREIGN KEY (User_ID) REFERENCES user (User_ID)
    ON UPDATE CASCADE
    ON DELETE RESTRICT
 ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

